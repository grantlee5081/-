def get_name(code: str) -> str:
    """回傳股票中文名稱，查無則回傳空字串。"""
    return TW_STOCK_NAMES.get(str(code).strip(), '')


def display(code: str, sep: str = ' ') -> str:
    """回傳『代號+名稱』格式，如 '2330 台積電'；查無名稱只回傳代號。"""
    name = get_name(code)
    return f"{code}{sep}{name}" if name else code


def display_list(codes: list[str]) -> list[str]:
    """批次轉換，保持原始順序。"""
    return [display(c) for c in codes]
